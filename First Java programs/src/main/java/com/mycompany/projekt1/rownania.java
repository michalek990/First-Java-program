   private static final double EPSILON = 1e-10;
    public static double[] lsolve(double[][] A, double[] b) {
        int n = b.length;
        for (int p = 0; p < n; p++) {

            int max = p;
            for (int i = p + 1; i < n; i++) {
                if (Math.abs(A[i][p]) > Math.abs(A[max][p])) {
                    max = i;
                }
            }
            double[] temp = A[p]; A[p] = A[max]; A[max] = temp;
            double   t    = b[p]; b[p] = b[max]; b[max] = t;
            if (Math.abs(A[p][p]) <= EPSILON) {
                throw new ArithmeticException("Blad");
            }

            // pivot within A and b
            for (int i = p + 1; i < n; i++) {
                double alpha = A[i][p] / A[p][p];
                b[i] -= alpha * b[p];
                for (int j = p; j < n; j++) {
                    A[i][j] -= alpha * A[p][j];
                }
            }
        }
        double[] x = new double[n];
        for (int i = n - 1; i >= 0; i--) {
            double sum = 0.0;
            for (int j = i + 1; j < n; j++) {
                sum += A[i][j] * x[j];
            }
            x[i] = (b[i] - sum) / A[i][i];
        }
        return x;
    }
    public static boolean funkcja_druga ( int n, double[][]  ABS, double[] ss ) {
        double eps = 1e-12; 
        int i, j, k;
        double m, s;
        for (i = 0; i < n - 1; i++) {
            for (j = i + 1; j < n; j++) {
                if (Math.abs(ABS[i][i]) < eps) return false;
                m = -ABS[j][i] / ABS[i][i];
                for (k = i + 1; k <= n; k++)
                    ABS[j][k] += m * ABS[i][k];
            }
        }
        for (i = n - 1; i >= 0; i--) {
            s = ABS[i][n];
            for (j = n - 1; j >= i + 1; j--)
                s -= ABS[i][j] * ss[j];
            if (Math.abs(ABS[i][i]) < eps) return false;
            ss[i] = s / ABS[i][i];
        }
        return true;
    }
